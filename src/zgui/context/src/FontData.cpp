auto getChineseGlyphRanges() -> std::vector<ImWchar>
{
    std::vector<ImWchar> ranges = {
            0x0020, 0x00FF, // Basic Latin + Latin Supplement
            0x0100, 0x01DC, // PINYIN
            0x2000, 0x206F, // General Punctuation
            0x3000, 0x30FF, // CJK Symbols and Punctuations, Hiragana, Katakana
            0x31F0, 0x31FF, // Katakana Phonetic Extensions
            0xFF00, 0xFFEF, // Half-width characters
            0xFFFD, 0xFFFD, // Invalid
    };
    std::wstring_convert<std::codecvt_utf8<char32_t>, char32_t> conv_utf8_utf32;
    std::u32string unicode_codepoints = conv_utf8_utf32.from_bytes(glyph_chinese);
    auto rangeFirst = unicode_codepoints.front();
    auto rangeLast = unicode_codepoints.front();
    for (const auto& codePoint : unicode_codepoints) {
        // auto imWchar = static_cast<ImWchar>(codePoint);
        if (codePoint - 1 <= rangeLast) {
            rangeLast = codePoint;
        } else {
            ranges.push_back(static_cast<ImWchar>(rangeFirst));
            ranges.push_back(static_cast<ImWchar>(rangeLast));
            rangeFirst = codePoint;
            rangeLast = codePoint;
        }
    }
    ranges.push_back(0);

    return ranges;
}
